<Br/>

Simply use the following short-code inside page or post content to display pricing table:
<br/>
<br/>
<code style="font-size: 14pt;">[ahm-pricing-table id=table_id template=template_name]</code>
<br/>
<br/>
Replace: <strong>table_id</strong> with <a href='edit.php?post_type=pricing-table'>the id of table you created</a><br/>

Replace: <strong>template_name</strong> with any of following available templates name:<br/>

<ul class="tt">
<li>override</li>
<li>rock</li>
<li>smooth</li>
<li>rtd-gray</li>
