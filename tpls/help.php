<Br/>

Simply use the following short-code inside page or post content to display pricing table:
<br/>
<br/>
<code style="font-size: 14pt;">[ahm-pricing-table id=table_id template=template_name]</code>
<br/>
<br/>
replace:<br/>
<strong>table_id</strong> with <a href='edit.php?post_type=pricing-table'>the id of table you created</a><br/>

replace template_name with any of following available template name:<br/>

